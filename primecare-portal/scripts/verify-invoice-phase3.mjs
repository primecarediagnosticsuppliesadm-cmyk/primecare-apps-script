#!/usr/bin/env node
/**
 * Invoice Phase 3 — PDF generation, storage, signed URL delivery.
 *
 * Usage:
 *   node scripts/verify-invoice-phase3.mjs
 *   node scripts/verify-invoice-phase3.mjs --remote
 */
import { createClient } from "@supabase/supabase-js";
import { createHash } from "crypto";
import { readFileSync, existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const SQL_PATH = resolve(root, "supabase/sql/invoice_system_phase3_migration.sql");
const API_PATH = resolve(root, "src/api/invoiceSupabaseApi.js");
const DOWNLOAD_PATH = resolve(root, "src/utils/invoiceDownload.js");
const EDGE_PATH = resolve(root, "supabase/functions/generate-invoice-pdf/index.ts");
const HQ_TENANT = "f168b98f-47a6-42c3-b788-24c00436fac2";
const REMOTE = process.argv.includes("--remote");

const results = [];

function pass(id, detail) {
  results.push({ id, status: "PASS", detail });
}
function fail(id, detail) {
  results.push({ id, status: "FAIL", detail });
}
function warn(id, detail) {
  results.push({ id, status: "WARN", detail });
}
function skip(id, detail) {
  results.push({ id, status: "SKIP", detail });
}

function loadEnvLocal() {
  const path = resolve(root, ".env.local");
  if (!existsSync(path)) return null;
  return Object.fromEntries(
    readFileSync(path, "utf8")
      .split("\n")
      .filter((l) => l && !l.startsWith("#"))
      .map((l) => {
        const i = l.indexOf("=");
        return [l.slice(0, i), l.slice(i + 1)];
      })
  );
}

function verifyStatic() {
  if (!existsSync(SQL_PATH)) fail("S-00", "invoice_system_phase3_migration.sql missing");
  else pass("S-00", "Phase 3 SQL migration exists");

  if (!existsSync(EDGE_PATH)) fail("S-01", "generate-invoice-pdf edge function missing");
  else pass("S-01", "generate-invoice-pdf edge function exists");

  const edge = existsSync(EDGE_PATH) ? readFileSync(EDGE_PATH, "utf8") : "";
  const api = existsSync(API_PATH) ? readFileSync(API_PATH, "utf8") : "";
  const dl = existsSync(DOWNLOAD_PATH) ? readFileSync(DOWNLOAD_PATH, "utf8") : "";

  if (/invoice_line_items/.test(edge) && !/order_items/.test(edge) && !/products/.test(edge)) {
    pass("S-10", "Edge function reads invoice_line_items snapshot only");
  } else {
    fail("S-10", "Edge function may read mutable catalog/order data");
  }

  if (/upsert:\s*true/.test(edge) && /\$\{tenantId\}\/\$\{invoiceId\}\.pdf/.test(edge)) {
    pass("S-11", "Storage path tenant_id/invoice_id.pdf with upsert");
  } else {
    fail("S-11", "Storage upload path or upsert missing");
  }

  if (
    /generateInvoicePdf/.test(api) &&
    /getInvoicePdfSignedUrl/.test(api) &&
    /ensureInvoicePdfAndGetSignedUrl/.test(api)
  ) {
    pass("S-12", "invoiceSupabaseApi PDF + signed URL surface");
  } else {
    fail("S-12", "invoiceSupabaseApi incomplete");
  }

  if (/downloadInvoicePdf/.test(dl) && /onPhase/.test(dl)) {
    pass("S-13", "Shared download helper with loading phases");
  } else {
    fail("S-13", "invoiceDownload.js incomplete");
  }

  if (/PrimeCare Diagnostics Supplies/.test(edge) && /Generated by PrimeCare/.test(edge)) {
    pass("S-14", "PDF sections: header + footer");
  } else {
    fail("S-14", "Required PDF sections missing");
  }

  const labOrdering = readFileSync(resolve(root, "src/pages/LabOrderingPage.jsx"), "utf8");
  if (/downloadInvoicePdf/.test(labOrdering) && !/coming soon/i.test(labOrdering)) {
    pass("S-20", "Lab Ordering invoice download wired");
  } else {
    fail("S-20", "Lab Ordering still has placeholder invoice actions");
  }

  const collections = readFileSync(resolve(root, "src/pages/CollectionsPage.jsx"), "utf8");
  if (/handleInvoiceDownload/.test(collections) && /getInvoicesForLabRead/.test(collections)) {
    pass("S-21", "Lab account timeline invoice download wired");
  } else {
    fail("S-21", "Collections lab invoice surface incomplete");
  }
}

async function login(env, email, password) {
  const sb = createClient(env.VITE_SUPABASE_URL, env.VITE_SUPABASE_ANON_KEY, {
    auth: { persistSession: false },
  });
  const { data, error } = await sb.auth.signInWithPassword({ email, password });
  if (error) return { sb: null, error: error.message };
  return { sb, error: null };
}

async function callGenerateEdge(env, token, invoiceId, force = false) {
  const res = await fetch(`${env.VITE_SUPABASE_URL}/functions/v1/generate-invoice-pdf`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${token}`,
      apikey: env.VITE_SUPABASE_ANON_KEY,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({ invoiceId, force }),
  });
  const body = await res.json().catch(() => ({}));
  return { ok: res.ok, status: res.status, body };
}

async function sha256FromSignedUrl(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`PDF fetch failed: ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  return createHash("sha256").update(buf).digest("hex");
}

async function verifyRemote(env) {
  const serviceKey = env.SUPABASE_SERVICE_ROLE_KEY;
  const adminEnv = await login(env, "qa.admin@primecare.test", "1234");
  if (adminEnv.error || !adminEnv.sb) {
    warn("R-01", `Admin auth failed: ${adminEnv.error || "unknown"}`);
    return;
  }

  const labEnv = await login(env, "qa.lab@primecare.test", "1234");
  const agentEnv = await login(env, "qa.agent@primecare.test", "1234");

  const readSb =
    serviceKey
      ? createClient(env.VITE_SUPABASE_URL, serviceKey, { auth: { persistSession: false } })
      : adminEnv.sb;
  if (!serviceKey) {
    warn("R-00", "SUPABASE_SERVICE_ROLE_KEY missing — using admin session for remote PDF tests");
  }

  const { data: invoiceRow } = await readSb
    .from("invoices")
    .select("id,tenant_id,lab_id,order_id,subtotal,tax_amount,total_amount,pdf_storage_path")
    .eq("tenant_id", HQ_TENANT)
    .eq("lab_id", "PILOT_LAB_014")
    .order("created_at", { ascending: false })
    .limit(1)
    .maybeSingle();

  if (!invoiceRow?.id) {
    warn("R-10", "No PILOT_LAB_014 invoice found for PDF tests");
    return;
  }

  const invoiceId = invoiceRow.id;
  const {
    data: { session },
  } = await adminEnv.sb.auth.getSession();
  const adminToken = session?.access_token;
  if (!adminToken) {
    warn("R-11", "Admin session token missing");
    return;
  }

  const gen1 = await callGenerateEdge(env, adminToken, invoiceId, true);
  if (!gen1.ok) {
    fail("R-20", `generate-invoice-pdf failed (${gen1.status}): ${gen1.body?.error || "unknown"}`);
    return;
  }
  pass("R-20", "generateInvoicePdf edge function succeeded");

  const { data: afterGen } = await readSb
    .from("invoices")
    .select("pdf_storage_path,status,subtotal,tax_amount,total_amount")
    .eq("id", invoiceId)
    .maybeSingle();

  const expectedPath = `${HQ_TENANT}/${invoiceId}.pdf`;
  if (afterGen?.pdf_storage_path === expectedPath) {
    pass("R-21", `pdf_storage_path set to ${expectedPath}`);
  } else {
    fail("R-21", `pdf_storage_path mismatch: ${afterGen?.pdf_storage_path}`);
  }

  const { data: signed, error: signErr } = await adminEnv.sb.storage
    .from("invoice-pdfs")
    .createSignedUrl(expectedPath, 120);

  if (signErr || !signed?.signedUrl) {
    fail("R-22", `Admin signed URL failed: ${signErr?.message || "no url"}`);
  } else {
    pass("R-22", "Admin signed URL created");
  }

  let hash1 = "";
  try {
    hash1 = await sha256FromSignedUrl(signed.signedUrl);
    pass("R-23", `PDF bytes retrieved (sha256 ${hash1.slice(0, 12)}…)`);
  } catch (err) {
    fail("R-23", err.message);
  }

  const totalsBefore = {
    subtotal: Number(invoiceRow.subtotal),
    tax: Number(invoiceRow.tax_amount),
    total: Number(invoiceRow.total_amount),
  };

  if (invoiceRow.order_id && serviceKey) {
    await readSb
      .from("orders")
      .update({ total_amount: 999999 })
      .eq("order_id", invoiceRow.order_id)
      .eq("tenant_id", HQ_TENANT);
  } else if (invoiceRow.order_id) {
    skip("R-30", "Order mutation test skipped (requires SUPABASE_SERVICE_ROLE_KEY)");
  }

  if (invoiceRow.order_id && serviceKey) {
    const { data: afterMutate } = await readSb
      .from("invoices")
      .select("subtotal,tax_amount,total_amount")
      .eq("id", invoiceId)
      .maybeSingle();

    if (
      Number(afterMutate?.subtotal) === totalsBefore.subtotal &&
      Number(afterMutate?.tax_amount) === totalsBefore.tax &&
      Number(afterMutate?.total_amount) === totalsBefore.total
    ) {
      pass("R-30", "Invoice snapshot totals unchanged after order mutation");
    } else {
      fail("R-30", "Invoice totals changed after order mutation");
    }
  }

  const gen2 = await callGenerateEdge(env, adminToken, invoiceId, Boolean(serviceKey && invoiceRow.order_id));
  if (!gen2.ok) {
    fail("R-31", `Regenerate failed: ${gen2.body?.error}`);
  } else {
    pass("R-31", serviceKey && invoiceRow.order_id ? "Regenerate after order mutation succeeded" : "PDF reuse path succeeded");
  }

  const { data: signed2 } = await adminEnv.sb.storage
    .from("invoice-pdfs")
    .createSignedUrl(expectedPath, 120);

  if (hash1 && signed2?.signedUrl) {
    const hash2 = await sha256FromSignedUrl(signed2.signedUrl);
    if (hash1 === hash2) {
      pass("R-32", serviceKey && invoiceRow.order_id ? "Deterministic PDF output after order mutation" : "Stable PDF bytes on signed URL re-fetch");
    } else if (!serviceKey || !invoiceRow.order_id) {
      fail("R-32", "PDF hash changed on re-fetch without order mutation");
    } else {
      fail("R-32", "PDF hash changed after order mutation regenerate");
    }
  }

  const storageSb = serviceKey
    ? createClient(env.VITE_SUPABASE_URL, serviceKey, { auth: { persistSession: false } })
    : adminEnv.sb;

  const { data: listBefore } = await storageSb.storage.from("invoice-pdfs").list(HQ_TENANT, {
    search: `${invoiceId}.pdf`,
  });
  const count1 = (listBefore || []).filter((o) => o.name === `${invoiceId}.pdf`).length;

  await callGenerateEdge(env, adminToken, invoiceId, false);
  await callGenerateEdge(env, adminToken, invoiceId, false);

  const { data: listAfter } = await storageSb.storage.from("invoice-pdfs").list(HQ_TENANT, {
    search: `${invoiceId}.pdf`,
  });
  const count2 = (listAfter || []).filter((o) => o.name === `${invoiceId}.pdf`).length;

  if (count1 >= 1 && count2 === 1) {
    pass("R-40", "Idempotent storage: single object for invoice PDF");
  } else if (!serviceKey && count1 === 0 && count2 === 0) {
    skip("R-40", "Storage list unavailable without service role — idempotency verified via edge reuse");
    const reuse = await callGenerateEdge(env, adminToken, invoiceId, false);
    if (reuse.ok && reuse.body?.reused) {
      pass("R-41", "Edge function returns reused=true when PDF already exists");
    }
  } else {
    fail("R-40", `Storage object count unexpected before=${count1} after=${count2}`);
  }

  if (labEnv.sb) {
    const { data: labProfile } = await labEnv.sb
      .from("profiles")
      .select("lab_id")
      .limit(1)
      .maybeSingle();
    const labUserLabId = String(labProfile?.lab_id || "").trim();
    const { data: labSigned, error: labSignErr } = await labEnv.sb.storage
      .from("invoice-pdfs")
      .createSignedUrl(expectedPath, 60);
    if (labUserLabId === invoiceRow.lab_id) {
      if (!labSignErr && labSigned?.signedUrl) {
        pass("R-50", `Lab own-invoice signed URL allowed (${labUserLabId})`);
      } else {
        fail("R-50", `Lab own-invoice denied: ${labSignErr?.message}`);
      }
    } else if (labSignErr || !labSigned?.signedUrl) {
      pass("R-51", `Cross-lab signed URL denied (${labUserLabId} vs ${invoiceRow.lab_id})`);
    } else {
      fail("R-51", "Cross-lab signed URL incorrectly allowed");
    }
  } else {
    skip("R-50", "qa.lab login unavailable");
    skip("R-51", "qa.lab login unavailable");
  }

  if (agentEnv.sb) {
    const { data: agentSigned, error: agentErr } = await agentEnv.sb.storage
      .from("invoice-pdfs")
      .createSignedUrl(expectedPath, 60);
    if (!agentErr && agentSigned?.signedUrl) {
      pass("R-53", "Authorized agent signed URL allowed");
    } else {
      skip("R-53", `Agent signed URL not available: ${agentErr?.message || "policy-dependent"}`);
    }
  } else {
    skip("R-53", "qa.agent login unavailable");
  }

  const { data: foreignTenant } = await readSb
    .from("invoices")
    .select("pdf_storage_path,tenant_id")
    .neq("tenant_id", HQ_TENANT)
    .not("pdf_storage_path", "is", null)
    .limit(1)
    .maybeSingle();

  if (foreignTenant?.pdf_storage_path && labEnv.sb) {
    const { data: deniedTenant, error: tenantErr } = await labEnv.sb.storage
      .from("invoice-pdfs")
      .createSignedUrl(foreignTenant.pdf_storage_path, 60);
    if (tenantErr || !deniedTenant?.signedUrl) {
      pass("R-52", "Cross-tenant signed URL denied");
    } else {
      fail("R-52", "Cross-tenant signed URL incorrectly allowed");
    }
  } else {
    skip("R-52", "No foreign-tenant PDF row for cross-tenant test");
  }
}

function printSummary() {
  const fails = results.filter((r) => r.status === "FAIL");
  const passes = results.filter((r) => r.status === "PASS");
  const warns = results.filter((r) => r.status === "WARN");
  const skips = results.filter((r) => r.status === "SKIP");

  console.log("\n=== Invoice Phase 3 Verification ===\n");
  for (const r of results) {
    console.log(`${r.status.padEnd(5)} ${r.id}  ${r.detail}`);
  }
  console.log(
    `\nSummary: PASS=${passes.length} FAIL=${fails.length} WARN=${warns.length} SKIP=${skips.length}`
  );
  if (fails.length) {
    console.log("\nINVOICE PHASE 3: FAIL");
    process.exit(1);
  }
  console.log("\nINVOICE PHASE 3: PASS");
}

verifyStatic();
if (REMOTE) {
  const env = loadEnvLocal();
  if (!env?.VITE_SUPABASE_URL) {
    warn("R-00", ".env.local missing — remote tests skipped");
  } else {
    await verifyRemote(env);
  }
} else {
  skip("R-ALL", "Remote tests skipped (use --remote)");
}
printSummary();
